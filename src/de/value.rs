// Deserializer wrapper for serde_json::Value

use crate::Config;
use serde::de::Visitor;

/// A wrapper around `serde_json::Value` that implements `Deserializer<'de>`
pub struct Value<'a> {
    /// The internal `serde_json::Value`
    pub value: serde_json::Value,
    /// Configuration for deserialization
    pub config: &'a Config,
}

impl<'a> Value<'a> {
    /// Creates a new `Value` from a `serde_json::Value` with custom config
    pub fn with_config(value: serde_json::Value, config: &'a Config) -> Self {
        Value { value, config }
    }
}

